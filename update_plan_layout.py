thin = Side(style="thin", color="D9D9D9")
medium = Side(style="medium", color="9E9E9E")


def fill(color):
    return PatternFill("solid", fgColor=color)


def border(style="thin"):
    side = medium if style == "medium" else thin
    return Border(top=side, bottom=side, left=side, right=side)


def style_cell(cell, bg=None, bold=False, color="000000", size=10, center=False):
    cell.font = Font(bold=bold, color=color, size=size)
    if bg:
        cell.fill = fill(bg)
    cell.border = border()
    cell.alignment = Alignment(
        horizontal="center" if center else "left",
        vertical="center",
        wrap_text=True,
    )


def add_day(ws, row, title, goal, rows):
    ws.merge_cells(start_row=row, start_column=1, end_row=row, end_column=9)
    c = ws.cell(row, 1, title)
    style_cell(c, COLORS["dark_blue"], True, COLORS["white"], 12, False)
    ws.row_dimensions[row].height = 26

    ws.merge_cells(start_row=row + 1, start_column=1, end_row=row + 1, end_column=9)
    c = ws.cell(row + 1, 1, goal)
    style_cell(c, COLORS["yellow"], True, "000000", 10, False)
    ws.row_dimensions[row + 1].height = 24

    headers = ["Раздел", "Упражнение", "Подходы", "Повторы", "Вес", "RPE / RIR", "Темп", "Отдых", "Заметки / альтернатива"]
    for col, header in enumerate(headers, 1):
        cell = ws.cell(row + 2, col, header)
        style_cell(cell, COLORS["navy"], True, COLORS["white"], 10, True)
    ws.row_dimensions[row + 2].height = 30

    start = row + 3
    for i, data in enumerate(rows):
        r = start + i
        for col, value in enumerate(data, 1):
            cell = ws.cell(r, col, value)
            bg = COLORS["light_blue"] if col in [3, 4, 5, 6, 7, 8] else COLORS["white"]
            if col == 9:
                bg = COLORS["purple"]
            style_cell(cell, bg, False, "000000", 10, col in [3, 4, 5, 6, 7, 8])
        ws.row_dimensions[r].height = 34

    return start + len(rows) + 1


