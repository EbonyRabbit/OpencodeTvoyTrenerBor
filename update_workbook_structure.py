thin = Side(style="thin", color="D9D9D9")


def fill(color):
    return PatternFill("solid", fgColor=color)


def style(cell, bg=None, bold=False, color="000000", size=10, center=False):
    cell.font = Font(bold=bold, color=color, size=size)
    if bg:
        cell.fill = fill(bg)
    cell.border = Border(top=thin, bottom=thin, left=thin, right=thin)
    cell.alignment = Alignment(
        horizontal="center" if center else "left",
        vertical="center",
        wrap_text=True,
    )


def remove_training_columns(wb):
    week_sheets = [name for name in wb.sheetnames if name.startswith("Гип W") or name.startswith("Инт W")]
    for name in week_sheets:
        ws = wb[name]
        # Original columns: M=Боль, O=Решение тренера, P=След. шаг.
        # Delete from right to left so indexes stay stable.
        for col in [16, 15, 13]:
            ws.delete_cols(col, 1)

        for row in range(1, ws.max_row + 1):
            for col in range(1, ws.max_column + 1):
                cell = ws.cell(row, col)
                if cell.value is not None:
                    cell.border = Border(top=thin, bottom=thin, left=thin, right=thin)
                    cell.alignment = Alignment(vertical="center", wrap_text=True)

        widths = [17, 31, 11, 12, 12, 10, 11, 10, 11, 12, 11, 10, 30]
        for i, width in enumerate(widths, 1):
            ws.column_dimensions[get_column_letter(i)].width = width
        ws.page_setup.fitToWidth = 1
        ws.page_setup.fitToHeight = 0


